// Input capture variables
uint32_t IC_Val1 = 0;
uint32_t IC_Val2 = 0;
uint32_t Difference = 0;
uint8_t Is_First_Captured = 0;
uint8_t overflow_count = 0;

float frequency = 0;
float duty_cycle = 0;

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM4 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
    {
        if (Is_First_Captured == 0)
        {
            IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
            Is_First_Captured = 1;
            overflow_count = 0;

            __HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
        }
        else if (Is_First_Captured == 1)
        {
            // Falling edge → measure pulse width
            IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
            uint32_t high_time = IC_Val2 - IC_Val1;  // Time signal was HIGH

            Is_First_Captured = 2;

            // Reconfigure to capture rising edge again
            __HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
        }
        else if (Is_First_Captured == 2)
        {
            // Second rising edge → full period captured
            uint32_t period_val = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
            uint32_t total_period = period_val - IC_Val1;
            uint32_t high_time   = IC_Val2   - IC_Val1;

            if (total_period > 0)
            {
                frequency  = 1000000.0f / total_period;       // Timer clock = 1MHz
                duty_cycle = (high_time * 100.0f) / total_period;
            }

            Is_First_Captured = 0; // Reset for next measurement
        }
    }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM4)
    {
        overflow_count++;
        if (overflow_count > 3)
        {
            // Signal lost or frequency too low — reset
            Is_First_Captured = 0;
            overflow_count = 0;
            frequency = 0;
            duty_cycle = 0;
        }
    }
}


