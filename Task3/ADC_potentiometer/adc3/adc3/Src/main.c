/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
    /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
    HAL_Init();

    /* Configure the system clock */
    SystemClock_Config();

    /* Initialize all configured peripherals */
    MX_GPIO_Init();
    MX_DMA_Init();          // FIX 3: DMA must be initialized BEFORE ADC
    MX_ADC1_Init();
    MX_USART1_UART_Init();

    /* USER CODE BEGIN 2 */
    HAL_ADCEx_Calibration_Start(&hadc1);   // FIX 4: Calibrate ADC before starting
    HAL_ADC_Start_DMA(&hadc1, (uint32_t*)&pot_v, 1);
    /* USER CODE END 2 */

    /* Infinite loop */
    while (1)
    {
        if (flag)
        {
            flag = 0;
            sprintf(value, "value=%d\r\n", pot_v);
            HAL_UART_Transmit(&huart1, (uint8_t*)value, strlen(value), 100);
            HAL_Delay(100);
        }
    }
}

